export function translate(lang, key, vars) {
  const dict = translations[lang] || translations.ru
  let str = dict[key] ?? translations.ru[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v)
    }
  }
  return str
}
